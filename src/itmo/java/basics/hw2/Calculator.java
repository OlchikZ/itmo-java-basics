package itmo.java.basics.hw2;

public class Calculator {
    public Calculator() {
    }

    // СУММА
    public int sum(int num1, int num2) {
        return num1 + num2;
    }

    public long sum(long num1, long num2) {
        return num1 + num2;
    }

    public double sum(double num1, double num2) {
        return num1 + num2;
    }

    // ВЫЧИТАНИЕ
    public int subtract(int num1, int num2) {
        return num1 - num2;
    }

    public long subtract(long num1, long num2) {
        return num1 - num2;
    }

    public double subtract(double num1, double num2) {
        return num1 - num2;
    }

    // УМНОЖЕНИЕ
    public int multiply(int num1, int num2) {
        return num1 * num2;
    }

    public long multiply(long num1, long num2) {
        return num1 * num2;
    }

    public double multiply(double num1, double num2) {
        return num1 * num2;
    }

    // ДЕЛЕНИЕ
    public int divide(int num1, int num2) {
        return num1 / num2; // предполагается, что b != 0
    }

    public long divide(long num1, long num2) {
        return num1 / num2;
    }

    public double divide(double num1, double num2) {
        return num1 / num2;
    }

}
